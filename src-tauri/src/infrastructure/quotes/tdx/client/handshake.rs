//! Hardcoded 3-step handshake from `pytdx/parser/setup_commands.py`.
//!
//! These packets are opaque to us — the server just expects them verbatim
//! after TCP connect. Body content is discarded.

use std::net::TcpStream;

use super::super::error::Result;
use super::frame::request;

const SETUP_1: &[u8] = &[
    0x0c, 0x02, 0x18, 0x93, 0x00, 0x01, 0x03, 0x00, 0x03, 0x00, 0x0d, 0x00, 0x01,
];
const SETUP_2: &[u8] = &[
    0x0c, 0x02, 0x18, 0x94, 0x00, 0x01, 0x03, 0x00, 0x03, 0x00, 0x0d, 0x00, 0x02,
];
const SETUP_3: &[u8] = &[
    0x0c, 0x03, 0x18, 0x99, 0x00, 0x01, 0x20, 0x00, 0x20, 0x00, 0xdb, 0x0f, 0xd5, 0xd0, 0xc9, 0xcc,
    0xd6, 0xa4, 0xa8, 0xaf, 0x00, 0x00, 0x00, 0x8f, 0xc2, 0x25, 0x40, 0x13, 0x00, 0x00, 0xd5, 0x00,
    0xc9, 0xcc, 0xbd, 0xf0, 0xd7, 0xea, 0x00, 0x00, 0x00, 0x02,
];

pub fn run(sock: &mut TcpStream) -> Result<()> {
    request(sock, SETUP_1)?;
    request(sock, SETUP_2)?;
    request(sock, SETUP_3)?;
    Ok(())
}
